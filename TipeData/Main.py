#Tipe data pada Python

#Integer
data_int = 123
print(type(data_int))

#Float ~ decimal
data_Float = 1.2
print(type(data_Float))

#String
data_str = "ini string"
print(type(data_str))

#Boolean ~ true/false
data_bool = True
print(type(data_bool))

#bilangan kompleks
data_comp = complex(3,5)
print(type(data_comp))

# tipe data dari C
# import package dari C
from ctypes import c_double

data_c_double = c_double(10.2)
print(type(data_c_double))